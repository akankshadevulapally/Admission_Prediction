import pandas as pd
import numpy as np
import sklearn
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import mean_absolute_error,mean_squared_error,r2_score

df=pd.read_csv('/content/Admission_Predict_Ver1.1.csv')
df = df.rename(columns={"Chance of Admit ": "Chance of Admit"})
df = df.rename(columns={"LOR ": "LOR"})
df=df.drop('Serial No.',axis=1)

X=df.drop(["Chance of Admit","Research","LOR","SOP"],axis=1).values
Y=df.iloc[:,-1].values

scale=StandardScaler()
X=scale.fit_transform(X)

x_train,x_test,y_train,y_test=train_test_split(X,Y,test_size=0.2,random_state=131,shuffle=True)

reg=LinearRegression().fit(x_train,y_train)

y_pred=reg.predict(x_test)
mae=mean_absolute_error(y_test,y_pred)
mse=mean_squared_error(y_test,y_pred)
rmse=np.sqrt(mse)
r2=r2_score(y_test,y_pred)
print('mean_absolute_error:',mae,'\n','mean_squared_error:',mse,'\n',\
      'root_mean_squared_error:',rmse,'\n','Score:',r2)

plt.scatter(y_test, y_pred, color='red')
plt.title('Test Output')
plt.xlabel('actual')
plt.ylabel('predicted')
plt.show()